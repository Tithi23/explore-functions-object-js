var color = 'skyblue';


if (color == 'blue') {
    console.log('color is blue');
}
else if (color == 'red') {
    console.log('color is red');
}
else if (color == 'green') {
    console.log('color is green');
}
else if (color == 'black') {
    console.log('color is black');
}
else if (color == 'yellow') {
    console.log('color is yellow');
}
else if (color == 'white') {
    console.log('color is white');
}
else {
    console.log('color hobe gray');
}


// switch
switch (color) {
    case 'blue':
        console.log('color is blue');
        break;
    case 'red':
        console.log('color is red');
        break;
    case 'white':
        console.log('color is white');
        break;
    case 'yellow':
        console.log('color is yellow');
        break;
    case 'green':
        console.log('color is green');
        break;
    default:
        console.log('color hobe na');
}